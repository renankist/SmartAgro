    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        // Aplica o validador
        JComponent[] components = new JComponent[]{tfdCaminhoSalvarBackup};
        VerificadorCampos verifier = new VerificadorCampos(components);

        if (!verifier.validaCampos()) {
            return;
        }

        try {
            Backup b = new Backup(tfdCaminhoSalvarBackup.getText());

            if (b.fazBackup()) {
                Mensagem.mostraInformacao("Sucesso", "Backup realizado com sucesso!");
                tfdCaminhoSalvarBackup.setText("");
            } else {
                Mensagem.mostraInformacao("Problema", "Erro ao tentar realizar backup!");
            }

            focus();
        } catch (FileNotFoundException ex) {
            Logger.getLogger(IfrmBackup.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

    private void jbtSelecionarDirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtSelecionarDirActionPerformed

        JFileChooser fileChooser = new JFileChooser();
        fileChooser.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY);
        fileChooser.setAcceptAllFileFilterUsed(false);

        int result = fileChooser.showOpenDialog(this);

        if (result == JFileChooser.APPROVE_OPTION) {

            tfdCaminhoSalvarBackup.setText(fileChooser.getSelectedFile().getAbsolutePath());

        }


    }//GEN-LAST:event_jbtSelecionarDirActionPerformed

